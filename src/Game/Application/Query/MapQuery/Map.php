     *      fields: string,
     *      moves: string,
     *      games_in_row: int,
     *      number_of_turn: int,
     *     winning_team: string,
     *  } $map
     *
     * @throws JsonException
     */
    public static function fromArray(array $map): self
    {
        /** @var array{
         * coordinates: array{row: int, column: int},
         * monk: array{id: int, team: string}|null,
         * color: string,
         * }[] $fields */
        $fields = json_decode($map['fields'], true, 512, JSON_THROW_ON_ERROR);
        /** @var array{
         *      id: int,
         *      team: string,
         *      moveFrom: array{
         *          row: string,
         *          column: string,
         *      },
         *      moveTo: array{
         *          row: string,
         *          column: string,
         *      },
         *      direction: string,
         * }[] $moves */
        $moves = json_decode($map['moves'], true, 512, JSON_THROW_ON_ERROR);

        return new self(
            array_map(static fn (array $field): Field => Field::fromArray($field), $fields),
            array_map(static fn (array $move): Move => Move::fromArray($move), $moves),
            $map['games_in_row'],
            $map['number_of_turn'],
            $map['winning_team'],
        );
    }

